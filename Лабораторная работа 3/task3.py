def count_letters(text):
    a = [""] * 33
    c = 0
    for i in text:
        if i.isalpha():
            c+=1
        for g in range(0, len(a)):
            i = i.lower()
            if i.isalpha():
                if a[g] == "" or i in a[g]:
                    a[g] = a[g] + i
                    break
    return [a, c]

