def calculate_frequency(array):
    a = array[0]
    c = array[1]
    d = dict()
    for i in a:
        if i:
            letter = i[0]
            size = len(i)
            d[letter] = round(size/c, 2)
            if d[letter] == 0.0 or d[letter] == 0:
                d[letter] = "0.00"
